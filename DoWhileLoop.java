public class DoWhileLoop {
    public static void main(String[] args) {
        int i = 0;

        //do-while loop, do print variables while condition not satisfies

        do{                                   //do following
            System.out.println(i);            //print i
            i++ ;                             //increment
        }while(i != 6);                       //till i = 5

    }
}
